package Demo;

public class ExemploBitABitOperadores {

	public static void main(String[] args) {
		int n1 = 89;
		int n2 = 60;
		System.out.println(n1 & n2);
		System.out.println(n1 | n2);
		System.out.println(n1 ^ n2);

		// exemplo bit a bit de operadores &(e) |(ou) ^(ou exclusivo)
		// Usado em programação de baixo nivel
	}

}
